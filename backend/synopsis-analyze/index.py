"""Анализ синопсиса писателя по профессиональным правилам издательского редактора"""
import json
import os
import urllib.request


SYNOPSIS_RULES = """
ПРАВИЛА ПРОФЕССИОНАЛЬНОГО СИНОПСИСА (для подачи в издательство):

1. ОБЪЁМ И ФОРМАТ
   - Стандарт: 1–2 страницы, 1 500–3 000 знаков с пробелами
   - Один абзац = одна смысловая единица
   - Никаких диалогов, цитат из текста, лирических вставок

2. ГЕРОЙ В ПЕРВОМ АБЗАЦЕ
   - Сразу: имя + возраст/статус + ключевая черта характера
   - Чего герой хочет (внешняя цель) и чего боится (внутренний конфликт)
   - Читатель должен понять, почему нам интересна именно эта история

3. КОНФЛИКТ И СТАВКИ
   - Центральный конфликт обозначен чётко и рано
   - Что герой потеряет, если не достигнет цели (ставки)
   - Есть и внешний конфликт (с миром, антагонистом) и внутренний (с собой)

4. СТРУКТУРА: НАЧАЛО → РАЗВИТИЕ → ПОВОРОТ → КУЛЬМИНАЦИЯ → ФИНАЛ
   - Все пять элементов присутствуют
   - Причинно-следственная логика: одно вытекает из другого
   - Нет «провалов» — непонятных прыжков между событиями

5. ФИНАЛ ОБЯЗАТЕЛЕН
   - Синопсис ВСЕГДА раскрывает, чем заканчивается история
   - Это не спойлер — это профессиональное требование
   - Редактор должен видеть всю дугу персонажа

6. ТРАНСФОРМАЦИЯ ГЕРОЯ
   - Герой в конце отличается от героя в начале
   - Внутренняя дуга: что он понял, как изменился, что потерял или обрёл

7. ВРЕМЯ И ГОЛОС
   - Настоящее время: «Анна идёт», «он узнаёт», «битва начинается»
   - Нейтральный авторский тон, без оценок («удивительный», «захватывающий»)
   - Активный залог, не пассивный

8. ПЕРСОНАЖИ
   - Называть по имени только 2–4 ключевых героя
   - Остальные: «старший брат», «наставница», «враг»
   - Не вводить новых персонажей в последней трети синопсиса

9. ТИПИЧНЫЕ ОШИБКИ
   - Аннотация вместо синопсиса (нет сюжета, только атмосфера)
   - Нет финала («что будет дальше — узнайте из книги»)
   - Прошедшее время вместо настоящего
   - Слишком много персонажей и имён
   - Нет внутреннего конфликта — только внешние события
   - Авторские оценки: «невероятный поворот», «трогательная сцена»
   - Слишком подробный пересказ деталей вместо сути
"""


def handler(event: dict, context) -> dict:
    """Анализирует синопсис книги и возвращает редакторские советы"""
    if event.get('httpMethod') == 'OPTIONS':
        return {
            'statusCode': 200,
            'headers': {
                'Access-Control-Allow-Origin': '*',
                'Access-Control-Allow-Methods': 'POST, OPTIONS',
                'Access-Control-Allow-Headers': 'Content-Type',
            },
            'body': ''
        }

    body = json.loads(event.get('body') or '{}')
    synopsis_text = body.get('synopsis', '').strip()

    if not synopsis_text:
        return {
            'statusCode': 400,
            'headers': {'Access-Control-Allow-Origin': '*'},
            'body': json.dumps({'error': 'Текст синопсиса не передан'})
        }

    api_key = os.environ.get('OPENAI_API_KEY', '')
    if not api_key:
        return {
            'statusCode': 500,
            'headers': {'Access-Control-Allow-Origin': '*'},
            'body': json.dumps({'error': 'API-ключ не настроен'})
        }

    char_count = len(synopsis_text)
    word_count = len(synopsis_text.split())

    prompt = f"""Ты — опытный редактор художественной литературы в российском издательстве. 
Тебе прислали синопсис книги для оценки. Проанализируй его строго по профессиональным правилам.

{SYNOPSIS_RULES}

---
СИНОПСИС АВТОРА ({char_count} знаков, ~{word_count} слов):
\"\"\"
{synopsis_text}
\"\"\"
---

Верни анализ строго в JSON:
{{
  "overall": "Общая редакторская оценка — 2-3 предложения. Тон: профессиональный, честный, но поддерживающий. Начни с того, что работает.",
  "volume_ok": true или false (соответствует ли объём норме 1500-3000 знаков),
  "tense_ok": true или false (написан ли в настоящем времени),
  "has_ending": true или false (есть ли финал истории),
  "score": целое число от 1 до 10,
  "checklist": [
    {{"id": "hero", "label": "Герой представлен в первом абзаце", "ok": true/false, "comment": "короткий комментарий если не ок, иначе null"}},
    {{"id": "conflict", "label": "Конфликт и ставки обозначены", "ok": true/false, "comment": "..."}},
    {{"id": "structure", "label": "Структура: начало → развитие → кульминация → финал", "ok": true/false, "comment": "..."}},
    {{"id": "ending", "label": "Финал раскрыт полностью", "ok": true/false, "comment": "..."}},
    {{"id": "arc", "label": "Внутренняя трансформация героя", "ok": true/false, "comment": "..."}},
    {{"id": "tense", "label": "Настоящее время", "ok": true/false, "comment": "..."}},
    {{"id": "characters", "label": "Не более 4 именованных персонажей", "ok": true/false, "comment": "..."}},
    {{"id": "tone", "label": "Нейтральный тон, без авторских оценок", "ok": true/false, "comment": "..."}}
  ],
  "top_issues": [
    {{"priority": "high/medium", "problem": "конкретная проблема в тексте", "fix": "как именно исправить — конкретный совет"}}
  ],
  "strengths": ["конкретная сильная сторона 1", "конкретная сильная сторона 2"]
}}

Важно: указывай на реальные места в тексте, не абстрактные советы. Если раздел хорош — скажи почему."""

    request_data = json.dumps({
        'model': 'gpt-4o',
        'messages': [{'role': 'user', 'content': prompt}],
        'response_format': {'type': 'json_object'},
        'temperature': 0.4,
        'max_tokens': 1800,
    }).encode('utf-8')

    req = urllib.request.Request(
        'https://api.openai.com/v1/chat/completions',
        data=request_data,
        headers={
            'Content-Type': 'application/json',
            'Authorization': f'Bearer {api_key}',
        },
        method='POST'
    )

    with urllib.request.urlopen(req, timeout=40) as resp:
        result = json.loads(resp.read().decode('utf-8'))

    content = result['choices'][0]['message']['content']
    analysis = json.loads(content)

    return {
        'statusCode': 200,
        'headers': {'Access-Control-Allow-Origin': '*'},
        'body': json.dumps(analysis, ensure_ascii=False)
    }
